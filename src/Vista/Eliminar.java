    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
    String cd = "";
    int k=0;
    ms1.setText("");
    try{
             cd=codigo.getText().trim();
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Ingrese un NÃºmero En Codigo", "Error", JOptionPane.ERROR_MESSAGE);  
             return;
        }
    if(codigo.getText().isEmpty()){
            ms1.setText("X");
            k++;
        } 
    if(k>0){
           JOptionPane.showMessageDialog(null, "Complete Los Campos Restantes", "Error", JOptionPane.ERROR_MESSAGE);
           return;
        }
    else{    
        
        int respuesta = JOptionPane.showConfirmDialog(null,"Seguro desea eliminar el medicamento");
        if(respuesta==JOptionPane.YES_OPTION){
            md.eliminar(cd);
        }
            }
    }//GEN-LAST:event_eliminarActionPerformed

